    public void getRecs() {
        SimpleDateFormat Date_Format = new SimpleDateFormat("yyyy-mm-dd");
        String date = Date_Format.format(txtShowDate.getDate());
        try {
            pst = con.prepareStatement("select a1.busNo,a1.seats,a1.date,a1.status,a2.customer,a2.mobile "
                    + " from seat a1 left join busbook a2 on a2.busNo=a1.busNo and a1.seats=1 ");
//            pst.setString(1, date);
            res = pst.executeQuery();
            ResultSetMetaData rsd = res.getMetaData();
            int c;
            c = rsd.getColumnCount();
            DefaultTableModel d=(DefaultTableModel)jTable1.getModel();
            d.setRowCount(0);
            while(res.next()) {
                Vector v2 = new Vector();
                for(int i=1;i<=c;i++) {
                   v2.add(res.getString("busNo"));
                   v2.add(res.getString("seats"));
                   v2.add(res.getString("status"));
                   v2.add(res.getString("customer"));
                   v2.add(res.getString("mobile"));
                   v2.add(res.getString("date"));
                }
                d.addRow(v2);
            }
        } catch (SQLException ex) {
            Logger.getLogger(booking.class.getName()).log(Level.SEVERE, null, ex);
        }
    }

