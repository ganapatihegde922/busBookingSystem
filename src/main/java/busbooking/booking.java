    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
       DefaultTableModel d1 = (DefaultTableModel)jTable1.getModel();
        int selected = jTable1.getSelectedRow();
        String busNo = d1.getValueAt(selected, 0).toString();
        
        String cust = txtCust.getText();
        String seat = txtSeat.getText();
        String mobile = txtMobile.getText();
        String bookDate = txtDate.getText();
        
        
        try {
               pst = con.prepareStatement("insert into busbook (busNo,seats,customer,mobile,date) values (?,?,?,?,?)");
           pst.setString(1, busNo);
           pst.setString(2, seat);
           pst.setString(3, cust);
           pst.setString(4, mobile);
           pst.setString(5, bookDate);
           pst.executeUpdate();
           
           pst1 = con.prepareStatement("update seat set status = ? where seats= ? ");
           pst1.setString(1, "Booked");
           pst1.setString(2, seat);
           pst1.executeUpdate();
           
           JOptionPane.showMessageDialog(this, "Seat is Added Successfully..");
           getRecs();  
           
           txtCust.setText("");
           txtSeat.setText("");
           txtMobile.setText("");
           txtDate.setText("");
           txtPrice.setText("");
           } 
           catch (SQLException ex) {
               Logger.getLogger(seat.class.getName()).log(Level.SEVERE, null, ex);
           }
        
        
    }//GEN-LAST:event_jButton2ActionPerformed

